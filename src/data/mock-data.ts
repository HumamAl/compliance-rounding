// 10. LOOKUP HELPERS
// ---------------------------------------------------------------------------
export const getFacilityById = (id: string): Facility | undefined =>
  facilities.find((f) => f.id === id);

export const getUserById = (id: string): User | undefined =>
  users.find((u) => u.id === id);

export const getRoundById = (id: string): Round | undefined =>
  rounds.find((r) => r.id === id);

export const getStandardByCode = (code: string): Standard | undefined =>
  standards.find((s) => s.code === code);

export const getDeficienciesByFacility = (facilityId: string): Deficiency[] =>
  deficiencies.filter((d) => d.facilityId === facilityId);

export const getDeficienciesByRound = (roundId: string): Deficiency[] =>
  deficiencies.filter((d) => d.roundId === roundId);

export const getRoundsByFacility = (facilityId: string): Round[] =>
  rounds.filter((r) => r.facilityId === facilityId);

export const getOpenDeficiencies = (): Deficiency[] =>
  deficiencies.filter((d) => d.status === "Open" || d.status === "In Progress" || d.status === "Overdue");

export const getCriticalDeficiencies = (): Deficiency[] =>
  deficiencies.filter((d) => d.priority === "Critical" && d.status !== "Resolved");
